public class Variavel {
    public static void main(String[] args) {
        
        int numero = 4;
        if (numero < 5 ) {
            System.out.println("Numero menor que 5");
        }else if (numero == 5) {
            System.out.println("Numero correto");   
        }else{
            System.out.println("Numero maior que 5");
        }

        System.out.println(numero);

    }
    
}
